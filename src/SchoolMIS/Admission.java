    public void updatecomboclass(){
        String sql ="select * from Class";
        try{
            pst=con.prepareStatement(sql);
            rs =pst.executeQuery();
            while(rs.next()){
                combo_class.addItem(rs.getString("class"));
                combo_stream.addItem(rs.getString("stream"));
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"!"+e);
            
        }
    }
