    public ArrayList<Student> studentList(){
       ArrayList<Student> studentsList = new ArrayList<>();
       
         String host="jdbc:mysql://localhost:3306/SchoolMIS?useTimezone=true&serverTimezone=UTC";
        String username ="schoolmis";
        String password="schoolmis";
        String driver ="com.msql.cj.jdbc.Driver";
        try{
             con =DriverManager.getConnection(host,username,password);
            stmt = con.createStatement();
            String query = "select * from Student_info ";
            stmt = con.createStatement();
             rs=stmt.executeQuery(query);
            Student student;
            while(rs.next()){
                student = new Student(rs.getInt("adm_Id"),rs.getString("Sir_name"),rs.getString("Middle_name"),rs.getString("Last_name"),rs.getString("Gender")
                        ,rs.getString("Date_of_birth"),rs.getString("TelNo"),rs.getString("class"),rs.getString("Stream"),rs.getString("date_of_admission"));
                studentsList.add(student);
                
            }
            
        }
        catch(Exception e){
              JOptionPane.showMessageDialog(null,"!"+e);
            
            
        }
       return studentsList;
        
    }
    
    public void show_student(){
        ArrayList<Student> list = studentList();
        DefaultTableModel model =(DefaultTableModel) jTable_student_list_display.getModel();
        Object[] row = new Object[10];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getadm_no();
            row[1] = list.get(i).getsir_name();
            row[2] = list.get(i).getmiddle_name();
            row[3] = list.get(i).getlast_name();
            row[4] = list.get(i).getgender();
            row[5] = list.get(i).getDOB();
            row[6] = list.get(i).getTelNO();                         
            row[7] = list.get(i).getmclass();
            row[8] = list.get(i).getstream();
            row[9] = list.get(i).getDOA();
            
            
            //row[8] = list.get(i).getDOA();
            model.addRow(row);
            
       
            
            
        }
        
    }

